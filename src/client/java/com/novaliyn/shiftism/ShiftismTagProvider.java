import net.minecraft.core.HolderLookup;
import net.minecraft.core.registries.Registries;
import net.minecraft.resources.Identifier;
import net.minecraft.tags.TagKey;
import net.minecraft.world.item.Item;

public class ShiftismTagProvider extends FabricTagProvider.ItemTagProvider {
	public ShiftismTagProvider(FabricDataOutput output, CompletableFuture<HolderLookup.Provider> registriesFuture) {
		super(output, registriesFuture);
	}

	@Override
	protected void addTags(HolderLookup.Provider wrapperLookup) {
			valueLookupBuilder(ShiftedItems.MIKU_REPAIR).add(ShiftedItems.miku_ingot).setReplace(true);
	}
}